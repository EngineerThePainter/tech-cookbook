#include "math_calc.hpp"

namespace math_calc {

double add(const double x1, const double x2) {
    return x1 + x2;
}

double sub(const double x1, const double x2) {
    return x1 - x2;
}

double mul(const double x1, const double x2) {
    return x1 * x2;
}

double div(const double x1, const double x2) {
    return x1/x2;
}

} // namespace math_calc